public class products_of_two_numbers {
    public static int product(int a, int b) {
        int multiply = a*b;
        return multiply;



    }
    public static void main(String[] args) {
        int a =8;
        int b= 4;
        int prod = product(a,b);
        System.out.println(prod);
        prod = product(20 , 30);
        System.out.println(prod);

    }
}
